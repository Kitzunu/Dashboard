// Escape a single value for use in a SQL INSERT statement
function escapeValue(val) {
  if (val === null || val === undefined) return 'NULL';
  if (Buffer.isBuffer(val)) return `0x${val.toString('hex')}`;
  if (typeof val === 'boolean') return val ? '1' : '0';
  if (typeof val === 'number' || typeof val === 'bigint') return String(val);
  if (val instanceof Date) {
    const p = n => String(n).padStart(2, '0');
    return `'${val.getFullYear()}-${p(val.getMonth()+1)}-${p(val.getDate())} ${p(val.getHours())}:${p(val.getMinutes())}:${p(val.getSeconds())}'`;
  }
  const s = String(val)
    .replace(/\\/g, '\\\\')
    .replace(/'/g, "\\'")
    .replace(/\n/g, '\\n')
    .replace(/\r/g, '\\r')
    .replace(/\0/g, '\\0');
  return `'${s}'`;
}

// Format a row as a SQL INSERT statement
function formatInsert(tableName, row) {
  const cols = Object.keys(row).map(c => `\`${c}\``).join(', ');
  const vals = Object.values(row).map(escapeValue).join(', ');
  return `INSERT INTO \`${tableName}\` (${cols}) VALUES (${vals});`;
}

// Check whether a table exists in the characters DB
async function tableExists(tableName) {
  const [rows] = await charPool.query(
    'SELECT COUNT(*) AS cnt FROM information_schema.tables WHERE table_schema = DATABASE() AND table_name = ?',
    [tableName]
  );
  return rows[0].cnt > 0;
}

async function generateDump(guid) {
  const lines = [];

  lines.push("-- IMPORTANT NOTE: THIS DUMPFILE IS MADE FOR USE WITH THE 'PDUMP' COMMAND ONLY - EITHER THROUGH INGAME CHAT OR ON CONSOLE!");
  lines.push("-- IMPORTANT NOTE: DO NOT apply it directly - it will irreversibly DAMAGE and CORRUPT your database! You have been warned!");
  lines.push(`-- Generated by AzerothCore Dashboard on ${new Date().toISOString()}`);
  lines.push('');

  // --- Simple GUID tables ---
  for (const table of GUID_TABLES) {
    if (!await tableExists(table)) continue;
    const [rows] = await charPool.query(`SELECT * FROM \`${table}\` WHERE guid = ?`, [guid]);
    for (const row of rows) lines.push(formatInsert(table, row));
  }

  // --- Pets (owner = guid) ---
  const [pets] = await charPool.query('SELECT * FROM `character_pet` WHERE owner = ?', [guid]);
  const petIds = pets.map(p => p.id);
  for (const pet of pets) lines.push(formatInsert('character_pet', pet));

  if (petIds.length > 0) {
    const petTables = [
      { name: 'character_pet_declinedname', col: 'id' },
      { name: 'pet_aura',           col: 'guid' },
      { name: 'pet_spell',          col: 'guid' },
      { name: 'pet_spell_cooldown', col: 'guid' },
    ];
    for (const { name, col } of petTables) {
      if (!await tableExists(name)) continue;
      const [rows] = await charPool.query(`SELECT * FROM \`${name}\` WHERE \`${col}\` IN (?)`, [petIds]);
      for (const row of rows) lines.push(formatInsert(name, row));
    }
  }

  // --- Mail (receiver = guid) ---
  const [mails] = await charPool.query('SELECT * FROM `mail` WHERE receiver = ?', [guid]);
  const mailIds = mails.map(m => m.id);
  for (const mail of mails) lines.push(formatInsert('mail', mail));

  let mailItemGuids = [];
  if (mailIds.length > 0) {
    const [mailItems] = await charPool.query('SELECT * FROM `mail_items` WHERE mail_id IN (?)', [mailIds]);
    mailItemGuids = mailItems.map(mi => mi.item_guid);
    for (const row of mailItems) lines.push(formatInsert('mail_items', row));
  }

  // --- Item instances (inventory + mail items) ---
  const [invRows] = await charPool.query('SELECT item FROM `character_inventory` WHERE guid = ?', [guid]);
  const inventoryItemGuids = invRows.map(r => r.item).filter(Boolean);

  const allItemGuids = [...new Set([...inventoryItemGuids, ...mailItemGuids])];
  if (allItemGuids.length > 0) {
    const [items] = await charPool.query('SELECT * FROM `item_instance` WHERE guid IN (?)', [allItemGuids]);
    for (const row of items) lines.push(formatInsert('item_instance', row));

    if (await tableExists('character_gifts')) {
      const [gifts] = await charPool.query('SELECT * FROM `character_gifts` WHERE item_guid IN (?)', [allItemGuids]);
      for (const row of gifts) lines.push(formatInsert('character_gifts', row));
    }
  }

  return lines.join('\n');
}

