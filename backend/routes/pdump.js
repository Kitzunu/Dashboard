// GET /api/pdump/default-path
// Returns the configured PDUMP_OUTPUT_PATH so the frontend can pre-fill the input
router.get('/default-path', requireGMLevel(2), (req, res) => {
  const dir = process.env.PDUMP_OUTPUT_PATH || '';
  res.json({ path: dir ? path.resolve(dir) : '' });
});

// GET /api/pdump/list-files
// Lists .sql/.txt dump files in the configured PDUMP_OUTPUT_PATH directory
router.get('/list-files', requireGMLevel(2), async (req, res) => {
  const dir = process.env.PDUMP_OUTPUT_PATH || '';
  if (!dir) return res.json({ files: [] });
  const resolved = path.resolve(dir);
  try {
    const entries = await fs.readdir(resolved, { withFileTypes: true });
    const files = entries
      .filter(e => e.isFile() && /\.(sql|txt)$/i.test(e.name))
      .map(e => ({ name: e.name, path: path.join(resolved, e.name) }));
    return res.json({ files });
  } catch {
    return res.json({ files: [] });
  }
});

// POST /api/pdump/load
// Body: { content?: string, filePath?: string, accountId: number, characterName?: string, characterGuid?: number }
//   content   → raw dump text sent from the browser
//   filePath  → path to a dump file on the server machine
//   accountId → target account to assign the character to
router.post('/load', requireGMLevel(2), async (req, res) => {
  const { content, filePath, accountId, characterName = '', characterGuid = 0 } = req.body || {};

  if (!accountId) return res.status(400).json({ error: 'accountId is required' });

  let dumpContent;
  try {
    if (filePath) {
      dumpContent = await fs.readFile(path.resolve(filePath), 'utf8');
    } else if (content) {
      dumpContent = content;
    } else {
      return res.status(400).json({ error: 'Provide either content or filePath' });
    }
  } catch (err) {
    return res.status(400).json({ error: `Cannot read dump file: ${err.message}` });
  }

  try {
    const result = await loadDump(dumpContent, {
      accountId: parseInt(accountId, 10),
      characterName,
      characterGuid: parseInt(characterGuid, 10) || 0,
    });
    audit(req, 'pdump.load', `character=${result.characterName} guid=${result.characterGuid} account=${accountId} source=${filePath ? `path:${path.resolve(filePath)}` : 'upload'}`);
    return res.json({ ok: true, ...result });
  } catch (err) {
    console.error('[pdump load] Error:', err);
    audit(req, 'pdump.load', `account=${accountId} error=${err.message}`, null, false);
    return res.status(500).json({ error: err.message });
  }
});

// POST /api/pdump/:guid  (write / export)
// Body: { filePath?: string }
//   filePath set  → save to server path, return JSON { ok, path, characterName }
//   filePath omit → return dump as downloadable text/plain attachment
router.post('/:guid', requireGMLevel(2), async (req, res) => {
  const guid = parseInt(req.params.guid, 10);
  if (!guid || guid <= 0) return res.status(400).json({ error: 'Invalid GUID' });

  const { filePath } = req.body || {};

  try {
    const [[char]] = await charPool.query('SELECT name FROM `characters` WHERE guid = ?', [guid]);
    if (!char) return res.status(404).json({ error: 'Character not found' });

    const dumpContent = await generateDump(guid);

    if (filePath) {
      const resolved = path.resolve(filePath);
      await fs.mkdir(path.dirname(resolved), { recursive: true });
      await fs.writeFile(resolved, dumpContent, 'utf8');
      audit(req, 'pdump.write', `character=${char.name} guid=${guid} path=${resolved}`);
      return res.json({ ok: true, path: resolved, characterName: char.name });
    }

    const fileName = `${char.name}_${guid}_${Date.now()}.sql`;
    audit(req, 'pdump.write', `character=${char.name} guid=${guid} output=download`);
    res.setHeader('Content-Type', 'text/plain; charset=utf-8');
    res.setHeader('Content-Disposition', `attachment; filename="${fileName}"`);
    return res.send(dumpContent);

  } catch (err) {
    console.error('[pdump write] Error:', err);
    if (!res.headersSent) return res.status(500).json({ error: err.message });
  }
});

